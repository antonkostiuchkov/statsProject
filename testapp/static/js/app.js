$(function() {


    var url = '/query/'
    var nameList = []
    var ageList = []

    $.ajax({
        url: url,
        type: 'GET',
        dataType: 'JSON',
        success: function (names) {
            $.each(names, function(i, name) {

                nameList.push(name.name)
                ageList.push(name.age)

                highCharts (nameList, ageList);
            });
        },
        error: function () {
            alert('Service error');
        }
    });


    highChartsLine ()
    highChartsZoomable ()
    highChartsCombo ()

// End of document.ready funtion
});



// Charts
function highCharts (nameList, ageList) {
    $('#container').highcharts({
        chart: {
            type: 'column'
        },
        title: {
            text: 'Title'
        },
        subtitle: {
            text: 'Test Subtitle'
        },
        xAxis: {
            categories: nameList,
            title: {
                text: null
            }
        },
        yAxis: {
            min: 0,
            max: 80,
            title: {
                text: 'Age',
                align: 'high'
            },
            labels: {
                overflow: 'justify'
            }
        },
        tooltip: {
            valueSuffix: ' years'
        },
        plotOptions: {
            bar: {
                dataLabels: {
                    enabled: true
                }
            }
        },
        legend: {
            layout: 'vertical',
            align: 'right',
            verticalAlign: 'top',
            x: -40,
            y: 80,
            floating: true,
            borderWidth: 1,
            backgroundColor: ((Highcharts.theme && Highcharts.theme.legendBackgroundColor) || '#FFFFFF'),
            shadow: true
        },
        credits: {
            enabled: false
        },
        series: [{
            name: 'Age',
            data: ageList
        }]
    });
};


